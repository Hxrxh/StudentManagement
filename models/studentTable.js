const { Sequelize, DataTypes } = require("sequelize");
const sequelize = require("../utils/db-connection");
const Students = sequelize.define("Students", {
  id: {
    type: DataTypes.INTEGER,
    allowNull: false,
    primaryKey: true,
    autoIncrement: true,
  },
  name: {
    type: DataTypes.STRING,
    allowNull: false,
  },
  email: {
    type: DataTypes.STRING,
    allowNull: false,
    unique: true,
  },
});

module.exports = Students;
